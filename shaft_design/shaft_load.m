function [Wt, Wr, Wa] = shaft_load(T_in, dp, alpha_n, beta)
%% ========================================================================
%  shaft_load.m  (v1.1 — 벡터입력 지원 명시)
%  헬리컬 기어 전달하중 3성분 (접선/반경/축). 
%  ------------------------------------------------------------------------
%  Wt=2T/dp, Wr=Wt*tan(alpha_t), Wa=Wt*tan(beta)
%  alpha_t = atan(tan(alpha_n)/cos(beta))
%  ------------------------------------------------------------------------
%  입력 (스칼라 또는 동일길이 벡터 — 기어 여러 개 한번에):
%    T_in    : 전달 토크 [N.mm]
%    dp      : 피치원 지름 [mm]
%    alpha_n : 치직각 압력각 [deg]
%    beta    : 나선각 [deg] (스퍼=0)
%  출력:
%    Wt,Wr,Wa : 접선/반경/축 방향 힘 [N]
% =========================================================================
    alpha_t = atand( tand(alpha_n) ./ cosd(beta) );
    Wt = 2 .* T_in ./ dp;
    Wr = Wt .* tand(alpha_t);
    Wa = Wt .* tand(beta);
end
