function [Y_J_p, Y_J_g] = Y_J(beta, N_p, N_g)
    % Geometry Factor (형상계수) for Helical Gear Bending, Y_J
    % 계산 방식:  Y_J = J' (Fig 14-7) × Modifying Factor (Fig 14-8)
    %   - J'  : 상대기어 75T 기준 형상계수            (Fig 14-7)
    %   - MF  : 실제 상대기어 잇수에 대한 보정계수      (Fig 14-8)
    %   - Y_J_p = J'(N_p) × MF(상대 = N_g)
    %   - Y_J_g = J'(N_g) × MF(상대 = N_p)
    % beta = 0 입력 시 Figure 14-6 (스퍼기어) 등가값 반환
    %
    % 입력
    %   beta : 헬릭스 각 [deg], 0 ≤ beta ≤ 35
    %   N_p  : 피니언 잇수 (12 ≤ N_p ≤ 500)
    %   N_g  : 기어 잇수   (12 ≤ N_g ≤ 500)
    % 출력
    %   Y_J_p : 피니언 형상계수
    %   Y_J_g : 기어 형상계수
    %
    % 검증: beta = 20°, N_p = 17, N_g = 50 →
    %       J'_p = 0.45, J'_g = 0.53, MF(상대17) = 0.94, MF(상대50) = 0.985
    %       Y_J_p = 0.45 × 0.985 = 0.443,  Y_J_g = 0.53 × 0.94 = 0.498
    %
    % 주의(데이터 보정): Fig 14-7/14-8 의 표는 beta = 20° 도면 판독값에 맞춰
    %   앵커링되어 있습니다(N=17, 50 / 상대=17, 50 격자점). 특히 Fig 14-8 의
    %   저잇수(상대 ≤ 30T) 구간은 이전 값(~0.92)이 판독값(0.94)보다 낮아
    %   약 +0.015 상향 보정했습니다. 다른 beta 의 절대값은 본인 도면으로 재확인 권장.

    % --- 입력 범위 검증 (Fig 14-7/14-6 의 실제 가로축 하한은 12T) ---
    if N_p < 12 || N_p > 500
        error('피니언 잇수 N_p = %g T 가 Fig 14-7 범위(12T ~ 500T)를 벗어났습니다.', N_p);
    end
    if N_g < 12 || N_g > 500
        error('기어 잇수 N_g = %g T 가 Fig 14-7 범위(12T ~ 500T)를 벗어났습니다.', N_g);
    end

    % --- 1단계: Figure 14-7 — 75T 상대기어 기준 J' ---
    Jp_prime = fig_14_7(beta, N_p);
    Jg_prime = fig_14_7(beta, N_g);

    % --- 2단계: Figure 14-8 — 실제 상대기어 보정계수 (MF) ---
    mult_p = fig_14_8(beta, N_g);   % 피니언 보정 (상대 = 기어)
    mult_g = fig_14_8(beta, N_p);   % 기어   보정 (상대 = 피니언)

    % --- 3단계: Y_J = J' × MF ---
    Y_J_p = Jp_prime * mult_p;
    Y_J_g = Jg_prime * mult_g;

    % --- 결과 출력 (디버깅용) ---
    % fprintf('Pinion: Geometry Factor for Bending  Y_J_p = %.4f\n', Y_J_p);
    % fprintf('Gear:   Geometry Factor for Bending  Y_J_g = %.4f\n', Y_J_g);
end

